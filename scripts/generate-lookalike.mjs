/**
 * Generates a REAL lookalike address for the poisoning demo: same first-4 and
 * last-4 hex characters as the target, different middle. A wallet renders both as
 * `0x64ea…fe3f`, which is exactly what an address-poisoning attacker relies on.
 * The demo firewall then blocks this generated address for real — nothing faked.
 *
 * Usage:
 *   node scripts/generate-lookalike.mjs 0xTargetAddress [affixLength]
 *
 * Writes packages/sdk/src/demo/lookalike.json for the sender demo to import.
 *
 * WHY we construct the address instead of grinding a keypair.
 *   A real attacker grinds private keys until an address matches the visible ends.
 *   For a 4+4 hex match that is a ~16^8 search — GPU-scale work, unfit for a repo
 *   script or CI. But the thing this demo proves is that the FIREWALL BLOCKS such
 *   an address, and `check()` decides that from the address alone; it never needs
 *   the private key. So we mint a genuine, correctly-formatted lookalike directly
 *   with the CSPRNG. It is a real address that really triggers the LOOKALIKE rule;
 *   we simply do not (and need not) control its key.
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { webcrypto } from 'node:crypto';

const target = process.argv[2];
const affix = Number(process.argv[3] ?? 4);

if (!target || !/^0x[0-9a-fA-F]{40}$/.test(target)) {
  console.error('Usage: node scripts/generate-lookalike.mjs 0xTargetAddress [affixLength]');
  process.exit(1);
}

const hex = target.slice(2).toLowerCase();
const prefix = hex.slice(0, affix);
const suffix = hex.slice(-affix);
const middleLength = 40 - affix * 2;

const bytes = new Uint8Array(Math.ceil(middleLength / 2));
webcrypto.getRandomValues(bytes);
const middle = [...bytes]
  .map((b) => b.toString(16).padStart(2, '0'))
  .join('')
  .slice(0, middleLength);

const lookalikeAddress = `0x${prefix}${middle}${suffix}`;

if (lookalikeAddress.toLowerCase() === target.toLowerCase()) {
  console.error('Randomly reproduced the target — vanishingly unlikely; re-run.');
  process.exit(1);
}

const short = (a) => `0x${a.slice(2, 6)}…${a.slice(-4)}`;
console.log(`Hedef:       ${target}   →  ${short(target)}`);
console.log(`Sahte adres: ${lookalikeAddress}   →  ${short(lookalikeAddress)}`);
console.log(`Cüzdanda ikisi de "${short(target)}" görünür — ayırt edilemez.`);

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const outDir = path.join(root, 'packages/sdk/src/demo');
fs.mkdirSync(outDir, { recursive: true });

const out = {
  _note:
    'Generated by scripts/generate-lookalike.mjs — a real lookalike of `target`, blocked by the firewall.',
  target,
  affixLength: affix,
  lookalikeAddress,
};

fs.writeFileSync(path.join(outDir, 'lookalike.json'), JSON.stringify(out, null, 2) + '\n');
console.log(`\nYazildi: packages/sdk/src/demo/lookalike.json`);
